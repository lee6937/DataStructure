package DataStructure;

public class Coin {
	 private int value;

	    public Coin(int value) {
	        this.value = value;
	    }
	    
	    public Coin() {
	    }
	    
	    public int value() {
	        return this.value;
	    }
	  
		public void setValue(int coin) {
			this.value = coin;
		}

}
